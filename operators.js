// Arithmetic
// *, +, -, /
console.log(10 + 5); // 15
console.log(10 % 3); // 1
console.log(2 ** 3); // 8
// Comparison
console.log(5 == "5"); // true (loose equality)
console.log(5 === "5"); // false (strict equality)

// >, <, <=, !=, !==
console.log(10 >= 7);
// true
// Logical
console.log(true && false); // false
console.log(true || false); // true
console.log(!true);
// false
